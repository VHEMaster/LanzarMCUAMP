void StartDefaultTask(void const * argument)
{
	
	HAL_TIM_Base_Start_IT(&htim7);
	GUI_Init();
	
	DAC_InitTask();
	
	while(CS4398_Init(&hi2c1, &hi2s1) != HAL_OK)
	{
		lcd_goto(0x40);
		lcd_print("CS4398 Init FAIL");
		osDelay(500);
	}
	//CS4398_SetMute(0);
	
	while(CS43L22_Init(&hi2c2, &hi2s2) != HAL_OK)
	{
		lcd_goto(0x40);
		lcd_print("CS43L22 Int FAIL");
		osDelay(500);
	}
	//CS43L22_SetMute(0);
	
	
	
	
	osDelay(500);
  MX_USB_DEVICE_Init();
	BT_Init();
	//WIFI_Init();
	
	HAL_GPIO_WritePin(PWR_EN_GPIO_Port, PWR_EN_Pin, GPIO_PIN_SET);
	//while(HAL_GPIO_ReadPin(PWR_GOOD_GPIO_Port, PWR_GOOD_Pin) == GPIO_PIN_RESET) 
		osDelay(10);
	
	lcd_clear();
	
	HAL_ADC_Start_DMA(&hadc1, (uint32_t*)ADC_VOLUME_DATA, ADC_DATA_SIZE);
	
	
	DAC_SetMute(0);
	DAC_HP_SetMute(0);
	
  for(;;)
  {
		HAL_RTC_GetTime(&hrtc, &sTime, RTC_FORMAT_BIN);
		HAL_RTC_GetDate(&hrtc, &sDate, RTC_FORMAT_BIN);
		
		
		if(ADC_VOLUME_CHANGED)
		{
			DAC_SetVolume(ADC_VOLUME);
			GUI_SetVolume(ADC_VOLUME);
			
			//Temporary since HP Pot not soldered
			DAC_HP_SetVolume(ADC_VOLUME);
			GUI_SetHPVolume(ADC_VOLUME);
			
			ADC_VOLUME_CHANGED = 0;
		}
		/*
		if(ADC_VOLUME_HP_CHANGED)
		{
			DAC_HP_SetVolume(ADC_VOLUME_HP);
			GUI_SetHPVolume(ADC_VOLUME_HP);
			ADC_VOLUME_HP_CHANGED = 0;
		}
		*/
		osDelay(10);
  }
}

